    private void cambiar()
    {
        Consulta consulta = new Consulta();
        Encriptar enc = new Encriptar();
        Usuario usuario = new Usuario();
        usuario = consulta.getUser(this.jTextField1.getText().toUpperCase().trim());
        if(usuario.getNombre() == null)
        {
            JOptionPane.showMessageDialog(this, "El usuario ingresado no existe, ingrese de nuevo");
        }
        else
        {
        if(usuario.getPassword().trim().equals(enc.codificar(String.valueOf(this.jPasswordField1.getPassword())))){
            if(String.valueOf(this.jPasswordField2.getPassword()).equals(String.valueOf(this.jPasswordField3.getPassword())))
            {
                usuario.setPassword(enc.codificar(String.valueOf(this.jPasswordField2.getPassword())));
                Modificacion modi = new Modificacion();
                int regModificados = modi.modificar(usuario);
                if (regModificados == 0)
                    JOptionPane.showMessageDialog(this, "Se produjo un error al intentar modificar el registro, cierre la base de datos y pruebe de nuevo");
                else
                    this.dispose();
            }
            else
                JOptionPane.showMessageDialog(this, "Ingreso mal la nueva clave, ingrese de nuevo");
        }else
            JOptionPane.showMessageDialog(this, "Clave incorrecta, ingrese nuevamente");
        }
    }
