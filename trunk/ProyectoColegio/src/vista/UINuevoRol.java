    private void jTable1MouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_jTable1MouseClicked
    {//GEN-HEADEREND:event_jTable1MouseClicked
// cuando se seleccione una fila, los datos de esa fila deben aparecer en los textbox de abajo solo si no se esta por cargar un nuevo elemento
        if(!this.guardar)
        {
            Iterator it = roles.iterator();
            Rol r = new Rol();
            while(it.hasNext())
            {
                r = (Rol)it.next();
                if(r.getIdrol() == Integer.parseInt(String.valueOf(this.jTable1.getValueAt(this.jTable1.getSelectedRow(),0))))
                {
                    this.jTCodigo.setText(String.valueOf(r.getIdrol()));
                    this.jTNombreRol1.setText(r.getNombre());
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

