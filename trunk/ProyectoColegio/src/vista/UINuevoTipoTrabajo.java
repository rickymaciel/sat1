    private void llenarTabla(TipoTrabajo t)
    {
        //De alguna forma obtengo una coleccion con los barrios cargados en la base de datos
        CListar listar = new CListar();
        trabajos = listar.hacerListado(t);
        listar = null;
        Iterator it = trabajos.iterator();
        // ahora obtengo el modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel)this.jTable1.getModel();
        String datos[] = new String[3];
        while(it.hasNext())
        {
            t = (TipoTrabajo)it.next();
            datos[0] = String.valueOf(t.getIdtipoTrabajo());
            datos[1] = t.getNombre();
            datos[2] = t.getLeyenda();
            modelo.addRow(datos);
        }
    }
    
    private int controlar()
    {
        int control = 0;
        if(this.jTNombreTipoTrabajo1.getText().trim().length()==0)
            control = 1;
        if(this.jTLeyenda.getText().trim().length()==0)
            control = 1;
        return control;
    }
    
    private void guardar()
    {
        //Iterator it = trabajos.iterator();
        TipoTrabajo tipoTrabajo = new TipoTrabajo();
        tipoTrabajo.setExiste("S");
        tipoTrabajo.setNombre(this.jTNombreTipoTrabajo1.getText().toUpperCase().trim());
        tipoTrabajo.setLeyenda(this.jTLeyenda.getText().trim().toUpperCase());
        this.guardar = false;
        this.jBGuardar.setEnabled(false);
        // aqui va el codigo que guarda en la base de datos
        CAlta alta = new CAlta();
        int respuesta = alta.hacerAlta(tipoTrabajo);
        if(respuesta != 0)
                JOptionPane.showMessageDialog(null,"Error en la carga de datos","Error",JOptionPane.ERROR_MESSAGE);
        else
                JOptionPane.showMessageDialog(null,"Los datos se cargaron correctamente","Carga Finalizada",JOptionPane.INFORMATION_MESSAGE);
        this.limpiar();
        this.llenarTabla(new TipoTrabajo());
        this.jTCodigo.setText("");
        this.jTNombreTipoTrabajo1.setText("");
        this.jTLeyenda.setText("");
    }
    
    
    private void modificar()
    {
        //Iterator it = trabajos.iterator();
        TipoTrabajo tipoTrabajo = new TipoTrabajo();
        tipoTrabajo.setExiste("S");
        tipoTrabajo.setNombre(this.jTNombreTipoTrabajo1.getText().toUpperCase().trim());
        tipoTrabajo.setLeyenda(this.jTLeyenda.getText().trim().toUpperCase());
        tipoTrabajo.setIdtipoTrabajo(Integer.parseInt(this.jTCodigo.getText()));
        this.guardar = false;
        this.jBGuardar.setEnabled(false);
        // aqui va el codigo que guarda en la base de datos
        Modificacion mod = new Modificacion();
        int respuesta = mod.modificar(tipoTrabajo);
        if(respuesta == 0)
                JOptionPane.showMessageDialog(null,"Error en la modificacion de datos","Error",JOptionPane.ERROR_MESSAGE);
        else
                JOptionPane.showMessageDialog(null,"Los datos se modificaron correctamente","Carga Finalizada",JOptionPane.INFORMATION_MESSAGE);
        this.limpiar();
        this.llenarTabla(new TipoTrabajo());
        this.jTCodigo.setText("");
        this.jTNombreTipoTrabajo1.setText("");
        this.jTLeyenda.setText("");
    }
    
    private void limpiar()
    {
        DefaultTableModel modelo = (DefaultTableModel)this.jTable1.getModel();
        while(this.jTable1.getRowCount() != 0)
                modelo.removeRow(0);
        modelo = null;
    }
    
    private void borrarRegistro(int fila)
    {
        Iterator it = trabajos.iterator();
        while(it.hasNext())
        {
            TipoTrabajo t = (TipoTrabajo)it.next();
            if(t.getIdtipoTrabajo() == Integer.parseInt(String.valueOf(this.jTable1.getValueAt(fila,0))))
            {
                Baja baja = new Baja();
                int respuesta = baja.eliminar(t);
                baja = null;
                if(respuesta == 0)
                    JOptionPane.showMessageDialog(null,"Error al dar de baja los datos","Error",JOptionPane.ERROR_MESSAGE);
                else
                    JOptionPane.showMessageDialog(null,"Los datos se eliminaron correctamente","Baja Completada",JOptionPane.INFORMATION_MESSAGE);
            }
            t = null;
        }
        it = null;
        this.limpiar();
        this.llenarTabla(new TipoTrabajo());
        this.jTCodigo.setText("");
        this.jTNombreTipoTrabajo1.setText("");
        this.jTLeyenda.setText("");
    }
    
