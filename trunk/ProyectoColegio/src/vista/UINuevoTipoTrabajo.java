    private void jTable1MouseClicked(java.awt.event.MouseEvent evt)//GEN-FIRST:event_jTable1MouseClicked
    {//GEN-HEADEREND:event_jTable1MouseClicked
// cuando se seleccione una fila, los datos de esa fila deben aparecer en los textbox de abajo solo si no se esta por cargar un nuevo elemento
        this.jTCodigo.setText("");
        this.jTLeyenda.setText("");
        this.jTNombreTipoTrabajo.setText("");
        this.jTNombreTipoTrabajo1.setText("");
        
        if(!this.guardar)
        {
            Iterator it = trabajos.iterator();
            TipoTrabajo t = new TipoTrabajo();
            while(it.hasNext())
            {
                t = (TipoTrabajo)it.next();
                if(t.getIdtipoTrabajo() == Integer.parseInt(String.valueOf(this.jTable1.getValueAt(this.jTable1.getSelectedRow(),0))))
                {
                    this.jTCodigo.setText(String.valueOf(t.getIdtipoTrabajo()).trim());
                    this.jTNombreTipoTrabajo1.setText(t.getNombre().trim());
                    if(t.getLeyenda() !=  null)
                        this.jTLeyenda.setText(t.getLeyenda().trim());
                }
            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

