    private void seleccionarTasa(String nombre)
    {
        Iterator it = this.tasas.iterator();
        while(it.hasNext())
        {
            Tasa t = (Tasa)it.next();
            if(t.getDenominacion().trim().equals(nombre.trim()))
            {
                this.jTextIndice.setText(String.valueOf(t.getIndice()));
                this.jTextMinimo.setText(String.valueOf(t.getTasaMinima()));
                break;
            }
            t = null;
        }
        it = null;
        this.calcularSubTotal();
    }
    
    private void seleccionarSuperficie(String nombre)
    {
        Iterator it = this.superficies.iterator();
        while(it.hasNext())
        {
            DetalleSuperficie d = (DetalleSuperficie)it.next();
            if(d.getTipoSuperficie().trim().equals(nombre.trim()))
            {
                this.jTextMetros.setText(String.valueOf(d.getSuerficie()));
                break;
            }
            d = null;
        }
        it = null;
        this.calcularSubTotal();
    }
    
    private void guardarDetalle()
    {
        System.out.println("El tamaño de la coleccion detalleFacturacion antes de enviarla es de: "+this.detalleFacturacion.size());
        this.nuevaCP.cargarDetalle(this.detalleFacturacion);
        this.dispose();
    }
    
    private void quitarLinea()
    {
       DefaultTableModel modelo = (DefaultTableModel)this.jTable1.getModel();
       Collection temp = new ArrayList();
       Iterator it = this.detalleFacturacion.iterator();
       int fila = Integer.parseInt(String.valueOf(this.jTable1.getSelectedRow()));
       DetalleCPParaFacturacion de = new DetalleCPParaFacturacion();
       de.setNombreTasa(String.valueOf(this.jTable1.getValueAt(fila, 1)));
       modelo.removeRow(this.jTable1.getSelectedRow());
       // ahaora quito el objeto de la coleccion
       while(it.hasNext())
       {
           DetalleCPParaFacturacion d = (DetalleCPParaFacturacion)it.next();
           if(!d.getNombreTasa().trim().equals(de.getNombreTasa().trim()))
               temp.add(d);
           d = null;
       }
       it = null;
       this.detalleFacturacion.clear();
       Iterator i = temp.iterator();
       while(i.hasNext())
       {
           DetalleCPParaFacturacion dd = (DetalleCPParaFacturacion)i.next();
           this.detalleFacturacion.add(dd);
           dd = null;
       }
       temp = null;
       i = null;
       de = null;
       this.calcularSubTotal();
    }
    
    
