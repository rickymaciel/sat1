    private void ini()
    {
        this.llenarComboTasas();
        this.llenarComboSuperficies();
        this.centrar();
        this.ajustarTamanioColumna();
        this.cargarDetalle();
    }
    
    private void llenarComboTasas()
    {
        CListar listar = new CListar();
        this.tasas = listar.hacerListado(new Tasa());
        Iterator it = this.tasas.iterator();
        while(it.hasNext())
        {
            Tasa t = (Tasa)it.next();
            this.jComboTasas.addItem(t.getDenominacion().trim());
            t = null;
        }
        listar = null;
        it = null;
    }
    
    private void llenarComboSuperficies()
    {
        Iterator it = this.superficies.iterator();
        while(it.hasNext())
        {
            DetalleSuperficie d = (DetalleSuperficie)it.next();
            this.jComboSuperficie.addItem(d.getTipoSuperficie().trim());
            d = null;
        }
        it = null;
    }
    
