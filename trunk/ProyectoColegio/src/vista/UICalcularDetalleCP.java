    private void cargarDetalle()
    {
        this.detalleFacturacion.clear();
        this.detalleFacturacion = this.nuevaCP.getDetalle();
        System.out.println("El detalle que recive de la constancia tiene "+this.detalleFacturacion.size()+" elementos");
        Iterator it = this.detalleFacturacion.iterator();
        DefaultTableModel  modelo=new DefaultTableModel();
        modelo=(DefaultTableModel) this.jTable1.getModel();
        String datos[] = new String[5];
        while(it.hasNext())
        {
            DetalleCPParaFacturacion d = (DetalleCPParaFacturacion)it.next();
            datos[1] = d.getNombreTasa().trim();
            datos[0] = String.valueOf(this.jTable1.getRowCount() + 1);
            datos[2] = d.getValorIndiceDeTasa() + " X "+d.getValorSuperficie();
            datos[3] = String.valueOf(d.getTasaMinimaAplicada());
            datos[4] = String.valueOf(d.getSubTotal());
            modelo.addRow(datos);
            d = null;
        }
        it = null;
        this.calcularTotal();
    }
    
    private void agregar()
    {
        DefaultTableModel  modelo=new DefaultTableModel();
        modelo=(DefaultTableModel) this.jTable1.getModel();
        String datos[] = new String[5];
        datos[1] = String.valueOf(this.jComboTasas.getSelectedItem()).trim().toUpperCase();
        datos[0] = String.valueOf(this.jTable1.getRowCount() + 1);
        datos[2] = this.jTextIndice.getText().trim() + " X " + this.jTextMetros.getText().trim();
        datos[3] = this.jTextMinimo.getText().trim();
        datos[4] = this.jTextSubtotal.getText().trim();
        modelo.addRow(datos);
        this.calcularTotal();
        DetalleCPParaFacturacion detalle = new DetalleCPParaFacturacion();
        detalle.setProducto(this.getProducto()); // producto
        detalle.setSubTotal(Double.parseDouble(this.jTextSubtotal.getText())); // subtotal
        detalle.setTipoSuperficie(String.valueOf(this.jComboSuperficie.getSelectedItem()).trim()); // tipo de superficie
        detalle.setValorSuperficie(Double.parseDouble(this.jTextMetros.getText())); // valor de la superficie
        detalle.setNombreTasa(String.valueOf(this.jComboTasas.getSelectedItem()).trim()); // nombre de la tasa
        detalle.setValorIndiceDeTasa(Double.parseDouble(this.jTextIndice.getText())); // indice de la tasa
        detalle.setTasaMinimaAplicada(Double.parseDouble(this.jTextMinimo.getText())); // tasa minima
        this.detalleFacturacion.add(detalle);
    }
    
    private Producto getProducto()
    {
        CListar listar = new CListar();
        Iterator it = this.tasas.iterator();
        Producto producto = new Producto();
        while(it.hasNext())
        {
            Tasa t = (Tasa)it.next();
            if(t.getDenominacion().trim().equals(String.valueOf(this.jComboTasas.getSelectedItem()).trim().toUpperCase()))
            {
                producto = listar.listar(t);
                break;
            }
            t = null;
        }
        it = null;
        listar = null;
        return producto;
    }
    
    private void calcularTotal()
    {
        double total = 0.0;
        for(int i=0; i<this.jTable1.getRowCount();i++)
            total += Double.parseDouble(String.valueOf(this.jTable1.getValueAt(i, 4)));
        NumberFormat tot = NumberFormat.getCurrencyInstance();
        this.jLabelTotal.setText(tot.format(total));
    }
    
    private void calcularSubTotal()
    {
        double subTotal = 0.0;
        double minimo = 0.0;
        double indice = 0.0;
        double sup = 0.0;
        indice = Double.parseDouble(String.valueOf(this.jTextIndice.getText()));
        if(this.jTextMinimo.getText().trim().length() != 0)
        {
            minimo = Double.parseDouble(this.jTextMinimo.getText());
            
            sup = Double.parseDouble(this.jTextMetros.getText());
            subTotal = indice * sup;
            if(subTotal < minimo)
                subTotal = minimo;
            Redondear r = new Redondear();
            subTotal = r.redondear(subTotal, 2);
            this.jTextSubtotal.setText(String.valueOf(subTotal));
        }
            
        else
            JOptionPane.showMessageDialog(null, "Debe ingresar un valor minimo","Falta valor minimo",JOptionPane.ERROR_MESSAGE);
    }

    
