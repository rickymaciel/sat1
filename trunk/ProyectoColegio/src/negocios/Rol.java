package negocios;

/**
 *
 * @author asus
 */
public class Rol {
    private int idrol;
    private String nombre;
    private String existe;
    
    public Rol()
    {
        
    }

    public int getIdrol() {
        return idrol;
    }

    public void setIdrol(int idrol) {
        this.idrol = idrol;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getExiste() {
        return existe;
    }

    public void setExiste(String existe) {
        this.existe = existe;
    }

    
}
