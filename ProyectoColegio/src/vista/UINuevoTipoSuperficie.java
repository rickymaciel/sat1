    private void llenarTabla(TipoSuperficie t)
    {
        //De alguna forma obtengo una coleccion con los barrios cargados en la base de datos
        CListar listar = new CListar();
        superficies = listar.hacerListado(t);
        listar = null;
        Iterator it = superficies.iterator();
        // ahora obtengo el modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel)this.jTable1.getModel();
        String datos[] = new String[3];
        while(it.hasNext())
        {
            t = (TipoSuperficie)it.next();
            datos[0] = String.valueOf(t.getIdtipoSuperficie());
            datos[1] = t.getNombre();
            datos[2] = String.valueOf(t.getModificador());
            modelo.addRow(datos);
        }
        t = null;
        it = null;
        datos = null;
        modelo = null;
    }
    
    private void limpiar()
    {
        DefaultTableModel modelo = (DefaultTableModel)this.jTable1.getModel();
        while(this.jTable1.getRowCount() != 0)
                modelo.removeRow(0);
        modelo = null;
    }
    
