    public void insertar(Escuela escuela)
    {
        this.jTNombreEscuelas.setText(escuela.getNombre().trim());        
        if (escuela.getLocalidad().getNombre().trim()!=null){
            this.jTLocalidad.setText(escuela.getLocalidad().getNombre().trim());
        }
        if (escuela.getDepartamento().getNombre().trim()!=null){
            this.jTDepartamento.setText(escuela.getDepartamento().getNombre().trim());
        }
        if (escuela.getProvincia().getNombre().trim()!=null){
            this.jTProvincia.setText(escuela.getProvincia().getNombre().trim()); 
        }
        if (escuela.getDireccion()!=null){            
            this.jTDireccion.setText(escuela.getDireccion().trim());
        }
        if (escuela.getTelefono()!=null){
            this.jTTelefono.setText(escuela.getTelefono().trim());
        }
        if (escuela.getMail()!=null){
            this.jTEmail.setText(escuela.getMail().trim());
        }
        if (escuela.getFax()!=null){
            this.jTFax.setText(escuela.getFax().trim());
        }
        if (escuela.getCodigoPostal()!=null){
            this.jTCp.setText(escuela.getCodigoPostal().trim());
        }
        
        this.jTCodigo.setText(String.valueOf(escuela.getIdescuela()).trim());        
        this.localidad = escuela.getLocalidad();
        this.xEscuela = escuela;
        this.xProvincia = escuela.getProvincia();
        this.xDepartamento = escuela.getDepartamento();
    }
    
