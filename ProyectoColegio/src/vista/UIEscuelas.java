    private void guardar()
    {
        
        Escuela e = new Escuela();
        e.setNombre(this.jTNombreEscuelas.getText().toUpperCase().trim());
        e.setCodigoPostal(this.jTCp.getText());
        e.setDepartamento(this.localidad.getDepartamento());
        e.setDireccion(this.jTDireccion.getText().toUpperCase().trim());
        e.setFax(this.jTFax.getText());
        e.setLocalidad(this.localidad);
        e.setMail(this.jTEmail.getText().toUpperCase().trim());
        e.setTelefono(this.jTTelefono.getText().trim());
        e.setExiste("S");
        e.setProvincia(this.localidad.getDepartamento().getProvincia());
        this.jBGuardar.setEnabled(false);
        
        CAlta alta = new CAlta();
        int respuesta = alta.hacerAlta(e);
        if(respuesta != 0)
              JOptionPane.showMessageDialog(null,"Error en la carga de datos","Error",JOptionPane.ERROR_MESSAGE);
        else
              JOptionPane.showMessageDialog(null,"Los datos se cargaron correctamente","Carga Finalizada",JOptionPane.INFORMATION_MESSAGE);
        this.limpiar();
        this.jTNombreEscuelas.requestFocus();
    }
    
