    private void borrarRegistro()
    {
        Collection escuelas = new ArrayList();
        CListar listar = new CListar();
        escuelas = listar.hacerListado(new Escuela());
        Iterator it = escuelas.iterator();
        listar = null;
        Escuela e = new Escuela();
        while(it.hasNext())
        {
            e = (Escuela)it.next();
            if(e.getIdescuela() == Integer.parseInt(String.valueOf(this.jTCodigo.getText().trim())))
            {
                Baja baja = new Baja();
                int respuesta = baja.eliminar(e);
                baja = null;
                if(respuesta == 0)
                    JOptionPane.showMessageDialog(null,"Error al dar de baja la localidad","Error",JOptionPane.ERROR_MESSAGE);
                else
                    JOptionPane.showMessageDialog(null,"La localidad se elimino correctamente","Baja Completada",JOptionPane.INFORMATION_MESSAGE);
            }
            e = null;
        }
        it = null;
        this.limpiar();
        this.jTNombreEscuelas.requestFocus();
    }
  
