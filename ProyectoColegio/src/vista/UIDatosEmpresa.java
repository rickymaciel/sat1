    private void guardar()
    {
        
        empresa.setCodigoPostal(this.jTCodigoPostal.getText().trim().toUpperCase());
        empresa.setCuit(this.jTCuit.getText().trim().toUpperCase());
        empresa.setDepartamento(this.barrio.getLocalidad().getDepartamento());
        empresa.setDireccion(this.jTDireccion2.getText().trim().toUpperCase());
        empresa.setExiste("S");
        empresa.setFax(this.jTFax.getText().trim().toUpperCase());
        empresa.setIngresosBrutos(this.jTIngresosBrutos.getText().trim().toUpperCase());
        empresa.setInicioActividades(this.jTInicioActividades.getText().trim());
        empresa.setIva(this.getIva());
        empresa.setLocalidad(this.barrio.getLocalidad());
        empresa.setNombre(this.jTNombreOrganizacion.getText().trim().toUpperCase());
        empresa.setProvincia(this.barrio.getLocalidad().getDepartamento().getProvincia());
        empresa.setTelefono(this.jTTelefono.getText().trim());
        empresa.setTitular(this.jTNombreTitular.getText().trim().toUpperCase());
        empresa.setBarrio(this.barrio);
        CAlta alta = new CAlta();
        int respuesta = alta.hacerAlta(empresa);
        if(respuesta != 0)
                JOptionPane.showMessageDialog(null,"Error en la carga de datos","Error",JOptionPane.ERROR_MESSAGE);
        else
        {
                JOptionPane.showMessageDialog(null,"Los datos se cargaron correctamente","Carga Finalizada",JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
        }
    }
    
    private Iva getIva()
    {
        Iva iva = new Iva();
        Iterator it = ivas.iterator();
        while(it.hasNext())
        {
            Iva i = (Iva)it.next();
            if(i.getNombre().trim().equals(String.valueOf(this.jCIva.getSelectedItem()).trim()))
                iva = i;
            i = null;
        }
        return iva;
    }
    
    private void cargarEmpresa()
    {
        Collection e = new ArrayList();
        CListar listar = new CListar();
        e = listar.hacerListado(this.empresa);
        Iterator it = e.iterator();
        while(it.hasNext())
        {
            this.empresa = (Empresa)it.next();
            this.jTNombreOrganizacion.setText(this.empresa.getNombre().trim());
            this.jTNombreTitular.setText(this.empresa.getTitular().trim());
            this.jTTelefono.setText(this.empresa.getTelefono().trim());
            this.jTFax.setText(this.empresa.getFax().trim());
            this.jTCodigoPostal.setText(this.empresa.getCodigoPostal().trim());
            this.jTProvincia.setText(this.empresa.getProvincia().getNombre().trim());
            this.jTDepartamento.setText(this.empresa.getDepartamento().getNombre().trim());
            this.jTLocalidad.setText(this.empresa.getLocalidad().getNombre().trim());
            this.jTBarrio.setText(this.empresa.getBarrio().getNombre().trim());
            this.jTDireccion2.setText(this.empresa.getBarrio().getNombre().trim());
            this.jTDireccion2.setText(this.empresa.getDireccion().trim());
            this.jTCuit.setText(this.empresa.getCuit().trim());
            this.jTIngresosBrutos.setText(this.empresa.getIngresosBrutos().trim());
            this.jTInicioActividades.setText(this.empresa.getInicioActividades().trim());
            this.jCIva.setSelectedItem(this.empresa.getIva().getNombre());
            this.barrio = this.empresa.getBarrio();
            this.barrio.setLocalidad(this.empresa.getLocalidad());
            this.barrio.getLocalidad().setDepartamento(this.empresa.getDepartamento());
            this.barrio.getLocalidad().getDepartamento().setProvincia(this.empresa.getProvincia());
        }
        e = null;
        listar = null;
        it = null;
    }
