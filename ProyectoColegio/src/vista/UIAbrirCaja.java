    private void jTextField1KeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyPressed
        char c = evt.getKeyChar();
        if (!((Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) ||(c == KeyEvent.VK_ENTER) ||(c == KeyEvent.VK_DELETE)  ||(c == KeyEvent.VK_ESCAPE) || (c == '.'))))
        {    
            getToolkit().beep();
            JOptionPane.showMessageDialog(null,"Debe introducir un valor valido ej: 15.50","Atencion",JOptionPane.ERROR_MESSAGE);
            this.jTextField1.setText("");
            this.jTextField1.requestFocus();
            evt.consume();
        }
        if(evt.getKeyCode() == 27)
        {
            dispose();
        }
        if(evt.getKeyCode() == 10)
        {
            this.jButton1.requestFocus();
        }
    }//GEN-LAST:event_jTextField1KeyPressed
   
