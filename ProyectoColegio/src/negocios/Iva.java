package negocios;


public class Iva {

    private int idiva;

    private String nombre;

    private String existe;

    public Iva () {
    }

    public String getExiste () {
        return existe;
    }

    public void setExiste (String val) {
        this.existe = val;
    }

    public int getIdiva () {
        return idiva;
    }

    public void setIdiva (int val) {
        this.idiva = val;
    }

    public String getNombre () {
        return nombre;
    }

    public void setNombre (String val) {
        this.nombre = val;
    }

}

